////////////////////////////////IMPORTANT//////////////////////////////////
//It is important to use the "const" qualifier. This prevents the PIC from
//putting the array in the "data" memory which is very limited. Think of
//the "Data" memory like RAM on a computer. "Data" memory is used for
//variables and arrays where the data needs to be updated continually.
//There is much less RAM on a computer than there is hard drive space.
//This is the same for a PIC.
//
//The "Const" instead places the hex data in the "Program" memory which is
//like the hard drive of the PIC. HOWEVER, this prevents us from making
//changes to the data. So the data below will not be able to be changed
//during runtime, however, it will be able to be read from. For the
//purposes of simply updating the bluetooth module, where we simply need
//to read the data, this is ok. So placing it in the "Program" memory is
//much more desirable instead of taking up our precious "Data" (RAM)
//space.
//
//Also note, that parts of this data can be stored into other variables
//that ARE in the "data" memory, and then the data can be manipulated, and
//used INPLACE of the original data. We still can't change the original data
//but we can change bytes and information by tricking the pic into sending
//other variables in replace of the pre-defined values here. This is only
//needed if you wish to make configuration changes to the chip on the fly.
//
//The trade off for using the "const" qualifier is that it takes less of
//our limited RAM resource, but we won't be able to make changes to it.
////////////////////////////////IMPORTANT//////////////////////////////////

